package corp;

class Address {
    int num;
    String street;
    String city;
    String province;
    int zipcode;
    public Address (int num, String street, String city, String province, int zipcode) {
        this.num = num;
        this.street = street;
        this.city = city;
        this.province = province;
        this.zipcode = zipcode;
    }
}
